package java1;

public class WhileDo {
    public static void main(String[] args) {
        /*
        Syntax of do while loop:
        do{
          //body
        }while(condition)
         */
        //Print value 1 to 5
        int n =1;
        do{
            System.out.println(n);
            n++;
        }while(n<=5);
    }
}
