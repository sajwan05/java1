package java1;

import java.util.Scanner;

public class LoopFor {
    public static void main(String[] args) {
        /*
//        Syntax of for loop :
          for(initialization;conditional;increment/decrement){
                //body
          }
         */
//        Print number 1 to 5
        for(int count=1;count!=6;count++){
            System.out.println(count);
        }
    }
}
